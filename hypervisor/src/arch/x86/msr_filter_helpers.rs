use std::num::NonZero;

use crate::HypervisorVmError;

/// Partitions the set of MSRs in `denied_sorted` into up to `max_num_subsets` subsets.
///
/// Each subset in the partition has the following properties:
/// - The subset is sorted
/// - if `s_min` and `s_max` is the smallest and largest element in the subset respectively
///   then the range `s_min..=s_max` does not intersect any other subset in the partition.
/// - When the range `0..=(s_max - s_min)` is compressed to a bitmap represented as a `&[u64]`
///   then the size of this bitmap in bytes is at most the given number `max_bitmap_repr_size`.
///
/// An error is returned if it is not possible to construct such a partition.
///
/// # Important
///
/// The provided arguments must satisfy the following requirements:
///
/// - `denied_sorted` is expected to be sorted and not contain any duplicate values.
/// - `max_bitmap_repr_size` must be a multiple of 8
pub fn partition_denied_msrs(
    denied_sorted: &[u32],
    max_num_subsets: NonZero<usize>,
    max_bitmap_repr_size: u32,
) -> Result<Box<[&[u32]]>, HypervisorVmError> {
    // This assumption simplifies the check for the bitmap size property
    assert_eq!(
        max_bitmap_repr_size % 8,
        0,
        "`max_bitmap_repr_size` is expected to be divisible by 8"
    );

    let max_num_partitions = max_num_subsets.get();
    let mut out = Vec::new();

    let mut remaining: &[u32] = denied_sorted;

    while !remaining.is_empty() {
        if out.len() == max_num_partitions {
            return Err(HypervisorVmError::MsrFilterSize);
        }

        let s_min = remaining[0];
        // Setting this as the maximum possible value in the subset guarantees the bitmap size property
        let max_smax = s_min.saturating_add((8 * max_bitmap_repr_size) - 1);

        // Find the index of the largest element >= s_min that is at most max_smax
        let end_idx = match remaining.binary_search(&max_smax) {
            Ok(idx) => idx,
            Err(idx) => idx - 1,
        };
        out.push(&remaining[..=end_idx]);
        remaining = remaining.get((end_idx + 1)..).unwrap_or_default();
    }
    Ok(out.into_boxed_slice())
}

