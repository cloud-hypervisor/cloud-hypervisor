    // This test mainly serves as a simple example of what the function does
    #[test]
    fn partition_denied_msrs_works() {
        let denied_msrs = [0, 1, 2, 42, 63, 64, 65, 130, 4000];
        let max_num_subsets = NonZero::new(5).unwrap();
        let max_bitmap_repr_size = 8;
        assert_eq!(
            partition_denied_msrs(&denied_msrs, max_num_subsets, max_bitmap_repr_size).unwrap(),
            Vec::from_iter([
                &denied_msrs[0..=4],
                &denied_msrs[5..=6],
                &denied_msrs[7..=7],
                &denied_msrs[8..=8]
            ])
            .into_boxed_slice()
        );
    }

